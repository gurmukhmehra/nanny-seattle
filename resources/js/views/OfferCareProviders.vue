    function ItemsCarousel()
    {        
		var owl = $('.owl-carousel');
              owl.owlCarousel({
                loop: true,
                nav: false,
                dots: true,
                autoplay:true,
                autoplayTimeout:2000,
                autoplayHoverPause:true,
