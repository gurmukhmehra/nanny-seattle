    {        
		var owl = $('.owl-carousel');
              owl.owlCarousel({
                loop: true,
                nav: false,
                dots: true,
                autoplay:true,
                autoplayTimeout:2000,
                autoplayHoverPause:true,
                margin: 10,
                responsive: {
                  0: {
                    items: 1
                  },
                  600: {
                    items: 3,
					      nav: false,
                  },
                  960: {
                    items: 4
                  },
                  1200: {
                    items: 4
                  }
                }
              });
              owl.on('mousewheel', '.owl-stage', function(e) {
                if (e.deltaY > 0) {
                  owl.trigger('next.owl');
                } else {
                  owl.trigger('prev.owl');
                }
                e.preventDefault();
              });
          }
		
